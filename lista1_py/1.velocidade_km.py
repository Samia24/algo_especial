#1. Leia uma velocidade em m/s, calcule e escreva esta velocidade em km/h. (Vkm/h = Vm/s * 3.6)

#Entrada
velocidade_metros = float(input('Informe a velocidade em m/s: '))

#Processamento
velocidade_km = velocidade_metros * 3.6

#Saída
print(f'A velocidade em quilômetros por hora é {velocidade_km} km/h.')

