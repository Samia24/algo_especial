#18. Leia o valor do raio de uma circunferência, calcule e escreva seu comprimento.(c = 2 * p * r)

#Entrada
raio = float(input('Informe o valor do raio de uma circunferência: '))

#Processamento
comprimento = 2 * 3.14 * raio

#Saída
print(f'\n>>> O comprimento da circunferência é de {comprimento:.1f}cm.')