#17. Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura)

#Entrada
base = float(input('Informe o valor da base de um retângulo: '))
altura = float(input('Informe o valor da altura de um retângulo: '))

#Processamento
area = base * altura

#Saída
print(f'\n>>> A área do retângulo é de {area:.1f}cm.')